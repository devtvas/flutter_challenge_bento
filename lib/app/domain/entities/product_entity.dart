class ProductEntity {
  final String id;
  final String name;
  final String description;
  final String category;
  final String image;
  final String price;
  final String material;
  final String department;

  ProductEntity({
    required this.id,
    required this.name,
    required this.description,
    required this.category,
    required this.image,
    required this.price,
    required this.material,
    required this.department,
  });
}
